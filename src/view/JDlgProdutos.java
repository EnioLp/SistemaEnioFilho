    public Produtos viewBean() {
        Produtos produtoNovo = new Produtos();

        if (this.produtos != null) {
            produtoNovo.setIdProduto(this.produtos.getIdProduto());
        }

        produtoNovo.setTitulo(jTextTitulo.getText());
        produtoNovo.setCodigoLivro(jTextCodigo.getText());
        produtoNovo.setAnoPublicado(Util.strToInt(jFmtAnoPublicado.getText()));
        produtoNovo.setGenero(jCboGenero.getSelectedItem().toString());
        produtoNovo.setAutor(jTextAutor.getText());
        produtoNovo.setEditora(jTextEditora.getText());
        
        String valorStr = jFmtPreco.getText().replace("R$ ", "").replace(".", "").replace(",", ".");
        if (!valorStr.isEmpty()) {
            produtoNovo.setPreco(new BigDecimal(valorStr));
        } else {
            produtoNovo.setPreco(BigDecimal.ZERO);
        }
        
        return produtoNovo;
    }
        public void beanView(Produtos produtos) {
        this.produtos = produtos;
        
        jTextTitulo.setText(produtos.getTitulo());
        jTextCodigo.setText(produtos.getCodigoLivro());
        jFmtAnoPublicado.setText(String.valueOf(produtos.getAnoPublicado()));
        jCboGenero.setSelectedItem(produtos.getGenero());
        jTextAutor.setText(produtos.getAutor());
        jTextEditora.setText(produtos.getEditora());
        jFmtPreco.setText(produtos.getPreco().toPlainString());
  
    }
    
