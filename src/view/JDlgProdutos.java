    private void jBtnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnConfirmarActionPerformed
        if (jTextTitulo.getText().trim().isEmpty()) {
            Util.mostrar("O campo 'Título' é obrigatório.");
            return;
        }
        
        Produtos produtoParaSalvar = viewBean();
        
        if (incluindo) {
            produtosDAO.insert(produtoParaSalvar);
            Util.mostrar("Produto incluído com sucesso!");
        } else {
            produtosDAO.update(produtoParaSalvar);
            Util.mostrar("Produto alterado com sucesso!");
        }

        defineEstadoInicial();
    }//GEN-LAST:event_jBtnConfirmarActionPerformed

